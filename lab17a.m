close all;clc;
x=[];
x(1)=0;
y=[];
y1(1)=3;
h=0.005;
y2(1)=1;
A=4;
B=0.5;
C=3;
D=1/3;
v11=[];
v12=[];
v13=[];
v14=[];
v21=[];
v22=[];
v23=[];
v24=[];
for i=2:1600
    x(i)=x(i-1)+h;
   v11(i)=A*(y1(i-1)).*(1-B*y2(i-1));
   v21(i)=C*(y2(i-1)).*(D.*(y1(i-1))-1);
  v12(i)=A.*(y1(i-1)+(h/.2*(v11(i-1)))).*(1-B.*(y2(i-1)+h/2.*(v21(i-1))));
   v22(i)=C.*((y2(i-1)+(h/.2)*v21(i-1))).*(D.*(y1(i-1)+(h/2.*(v11(i-1))))-1);
   v13(i)=A.*(y1(i-1)+(h/.2*(v12(i-1)))).*(1-B.*(y2(i-1)+h/2.*(v22(i-1))));
   v23(i)=C.*((y2(i-1)+(h/.2)*v22(i-1))).*(D.*(y1(i-1)+(h/2.*(v12(i-1))))-1);
   v14(i)=A.*(y1(i-1)+(h.*(v13(i-1)))).*(1-B.*(y2(i-1)+h.*(v23(i-1))));
   v24(i)=C.*((y2(i-1)+(h)*v23(i-1))).*(D.*(y1(i-1)+(h.*(v13(i-1))))-1);
   y1(i)=y1(i-1)+(h./6)*(v11(i)+2.*v12(i)+2.*v13(i)+v14(i));
   y2(i)=y2(i-1)+(h./6)*(v21(i)+2.*v22(i)+2.*v23(i)+v24(i));

end

plot(x,y1);
hold on;
plot(x,y2);
title('Amplitude of predator and prey');
legend('Y1 vs x','Y2 vs x');
hold off;
figure;
plot(y1,y2);
legend('Y1 vs Y2');




