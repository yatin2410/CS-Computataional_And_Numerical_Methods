function y = geo_func(x)
    y = cos(x);
    %y = x^6 - x - 1;
    %y = x^3-x.^2-x-1;
    %y = x-(1+0.3*cos(x));
    %y=cos(x)-(0.5+sin(x));
    %y=x-exp(-x);
    %y=exp(-x)-sin(x);
    %y=x.^2-2*x-2;
    %y=x.^4-x-1;
    %y=exp(x)-x-2;
    %y=1-x+sin(x);
    %y=x-tan(x);
    %y=x*(x-1);
    %y=-x*(x-1);
    %y=0.000001+x*(x-1)*(x-1);
end
